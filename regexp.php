<h1>Основи мови регулярних виразів в PHP</h1>

<h2>Спецсимволи</h2>
<p>Спецсимволи мають спеціальні значення в регулярних виразах. Щоб шукати їх як звичайні символи, потрібно екранувати їх за допомогою зворотного слеша <code>\</code>. Ось деякі з основних спецсимволів:</p>
<ul>
    <li><code>.</code> — будь-який символ, крім нового рядка.</li>
    <li><code>^</code> — початок рядка.</li>
    <li><code>$</code> — кінець рядка.</li>
    <li><code>\d</code> — будь-яка цифра (еквівалентно [0-9]).</li>
    <li><code>\D</code> — будь-який символ, крім цифри.</li>
    <li><code>\w</code> — будь-яка буквено-цифрова літера (еквівалентно [a-zA-Z0-9_]).</li>
    <li><code>\W</code> — будь-який символ, крім буквено-цифрового.</li>
    <li><code>\s</code> — будь-який пробільний символ (пробіл, табуляція, новий рядок).</li>
    <li><code>\S</code> — будь-який символ, крім пробільного.</li>
</ul>

<h2>Групи</h2>
<p>Групи дозволяють об'єднувати частини виразу та працювати з ними як з єдиним блоком.</p>
<ul>
    <li><code>(...)</code> — захоплююча група. Наприклад, <code>(abc)</code> збігається з "abc" і захоплює цю частину для подальшого використання.</li>
    <li><code>(?:...)</code> — незахоплююча група. Наприклад, <code>(?:abc)</code> збігається з "abc", але не захоплює цю частину.</li>
</ul>

<h2>Квантифікатори</h2>
<p>Квантифікатори визначають кількість повторень попереднього символу або групи.</p>
<ul>
    <li><code>*</code> — 0 або більше разів.</li>
    <li><code>+</code> — 1 або більше разів.</li>
    <li><code>?</code> — 0 або 1 раз.</li>
    <li><code>{n}</code> — рівно n разів.</li>
    <li><code>{n,}</code> — n або більше разів.</li>
    <li><code>{n,m}</code> — від n до m разів.</li>
</ul>

<h2>Якорі</h2>
<p>Якорі використовуються для прив'язки виразу до певної позиції в тексті.</p>
<ul>
    <li><code>^</code> — початок рядка.</li>
    <li><code>$</code> — кінець рядка.</li>
    <li><code>\b</code> — межа слова.</li>
    <li><code>\B</code> — не межа слова.</li>
</ul>

<h2>Використання регулярних виразів у PHP</h2>
<p>У PHP регулярні вирази використовуються за допомогою функцій <code>preg_*</code>.</p>

<h3>Приклад використання <code>preg_match</code></h3>
<pre>
    <code>
        &lt;?php
        $pattern = "/^hello/";
        $string = "hello world";
        if (preg_match($pattern, $string)) {
            echo "Збіг знайдено!";
        } else {
            echo "Збіг не знайдено.";
        }
        ?&gt;
    </code>
</pre>

<h3>Приклад використання <code>preg_replace</code></h3>
<pre>
    <code>
        &lt;?php
        $pattern = "/world/";
        $replacement = "PHP";
        $string = "hello world";
        $result = preg_replace($pattern, $replacement, $string);
        echo $result; // Виведе "hello PHP"
        ?&gt;
    </code>
</pre>

<p>Ці приклади демонструють основні принципи роботи з регулярними виразами в PHP. Для детальнішого вивчення ви можете ознайомитися з <a href="https://www.php.net/manual/en/book.pcre.php">документацією PHP</a>.</p>

